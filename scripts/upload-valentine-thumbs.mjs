#!/usr/bin/env node
// Generates 400px thumbnails from the previously-compressed pose WebPs
// (located in $CLAUDE_JOB_DIR/poses-compressed or arg[0]) and uploads
// them to Vercel Blob. Merges the resulting URLs into
// src/data/valentine-poses.ts under a new `thumbUrl` field per entry,
// preserving the existing 900px `url`.
//
// Usage:
//   node scripts/upload-valentine-thumbs.mjs [compressedDir]

import 'dotenv/config';
import dotenv from 'dotenv';
dotenv.config({ path: '.env.local', override: true });

import fs from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';
import { put } from '@vercel/blob';

if (!process.env.BLOB_READ_WRITE_TOKEN) {
  console.error('BLOB_READ_WRITE_TOKEN missing');
  process.exit(1);
}

const SRC = path.resolve(
  process.argv[2] ??
    (process.env.CLAUDE_JOB_DIR
      ? path.join(process.env.CLAUDE_JOB_DIR, 'poses-compressed')
      : 'tmp/poses-compressed'),
);
const MANIFEST_TS = path.resolve('src/data/valentine-poses.ts');
const THUMB_MAX = 400;
const THUMB_QUALITY = 72;

const compressed = JSON.parse(await fs.readFile(path.join(SRC, 'manifest.json'), 'utf8'));

// Pull the existing per-filename url map out of the TS module so we can
// merge thumbUrls without re-uploading the full-size blobs.
const tsSource = await fs.readFile(MANIFEST_TS, 'utf8');
const arrMatch = tsSource.match(/VALENTINE_POSES[^=]*=\s*(\[[\s\S]*?\]) as const/);
if (!arrMatch) {
  console.error('Could not locate VALENTINE_POSES array in', MANIFEST_TS);
  process.exit(1);
}
const existing = JSON.parse(arrMatch[1]);
const existingByFilename = new Map(existing.map((e) => [e.filename, e]));

console.log(`Generating + uploading thumbs for ${compressed.entries.length} poses`);

const merged = [];
let i = 0;
let totalThumbBytes = 0;

for (const e of compressed.entries) {
  i += 1;
  const existingEntry = existingByFilename.get(e.src);
  if (!existingEntry) {
    console.warn(`\n  skip ${e.src}: not in existing manifest`);
    continue;
  }

  const fullPath = path.join(SRC, e.out);
  const thumbBuf = await sharp(fullPath)
    .resize({ width: THUMB_MAX, height: THUMB_MAX, fit: 'inside', withoutEnlargement: true })
    .webp({ quality: THUMB_QUALITY, effort: 6 })
    .toBuffer();
  totalThumbBytes += thumbBuf.length;

  const thumbKey = e.out.replace(/\.webp$/, '-thumb.webp');
  const blob = await put(`valentine/poses/${thumbKey}`, thumbBuf, {
    access: 'public',
    contentType: 'image/webp',
    addRandomSuffix: true,
    allowOverwrite: false,
  });

  merged.push({
    filename: existingEntry.filename,
    url: existingEntry.url,
    thumbUrl: blob.url,
  });
  process.stdout.write(`\r  [${i}/${compressed.entries.length}] ${e.src}`.padEnd(80));
}
process.stdout.write('\n');

merged.sort((a, b) => a.filename.localeCompare(b.filename));

const out = `// Auto-generated by scripts/upload-valentine-poses.mjs +
// scripts/upload-valentine-thumbs.mjs. Maps the original screenshot
// filename (also the experiencedPositionIds key in Mongo) to its Vercel
// Blob URLs — \`url\` is the 900 px modal-size image, \`thumbUrl\` is the
// 400 px grid thumbnail. Do not hand-edit — re-run the upload scripts if
// the catalogue changes.

export interface ValentinePoseEntry {
  filename: string;
  url: string;
  thumbUrl: string;
}

export const VALENTINE_POSES: readonly ValentinePoseEntry[] = ${JSON.stringify(merged, null, 2)} as const;

export const VALENTINE_POSES_BY_FILENAME: Readonly<Record<string, ValentinePoseEntry>> =
  Object.fromEntries(VALENTINE_POSES.map((p) => [p.filename, p]));
`;

await fs.writeFile(MANIFEST_TS, out);
console.log(`Uploaded ${merged.length} thumbs (${(totalThumbBytes / 1024).toFixed(0)} KB total)`);
console.log(`Updated ${MANIFEST_TS}`);
