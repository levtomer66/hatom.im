#!/usr/bin/env node
// Uploads the compressed valentine poses to Vercel Blob and emits
// src/data/valentine-poses.ts — a manifest that maps the original PNG
// filename (which is also the `experiencedPositionIds` key in Mongo) to
// its Blob URL.
//
// Usage: node scripts/upload-valentine-poses.mjs <compressedDir>
//   compressedDir defaults to $CLAUDE_JOB_DIR/poses-compressed

import 'dotenv/config';
import dotenv from 'dotenv';
dotenv.config({ path: '.env.local', override: true });

import fs from 'node:fs/promises';
import path from 'node:path';
import { put } from '@vercel/blob';

if (!process.env.BLOB_READ_WRITE_TOKEN) {
  console.error('BLOB_READ_WRITE_TOKEN missing from .env.local');
  process.exit(1);
}

const SRC = path.resolve(
  process.argv[2] ??
    (process.env.CLAUDE_JOB_DIR
      ? path.join(process.env.CLAUDE_JOB_DIR, 'poses-compressed')
      : 'tmp/poses-compressed'),
);
const MANIFEST_OUT = path.resolve('src/data/valentine-poses.ts');

const manifestPath = path.join(SRC, 'manifest.json');
const manifest = JSON.parse(await fs.readFile(manifestPath, 'utf8'));

console.log(`Uploading ${manifest.entries.length} files from ${SRC}`);

const entries = [];
let i = 0;
for (const e of manifest.entries) {
  i += 1;
  const fileBuf = await fs.readFile(path.join(SRC, e.out));
  const blob = await put(`valentine/poses/${e.out}`, fileBuf, {
    access: 'public',
    contentType: 'image/webp',
    addRandomSuffix: true,
    allowOverwrite: false,
  });
  entries.push({ filename: e.src, url: blob.url });
  process.stdout.write(`\r  [${i}/${manifest.entries.length}] ${e.src}`.padEnd(80));
}
process.stdout.write('\n');

entries.sort((a, b) => a.filename.localeCompare(b.filename));

const header = `// Auto-generated by scripts/upload-valentine-poses.mjs.
// Maps the original screenshot filename (also the experiencedPositionIds
// key in Mongo) to its Vercel Blob URL. Do not hand-edit — re-run the
// upload script if the catalogue changes.

export interface ValentinePoseEntry {
  filename: string;
  url: string;
}

export const VALENTINE_POSES: readonly ValentinePoseEntry[] = ${JSON.stringify(entries, null, 2)} as const;

export const VALENTINE_POSES_BY_FILENAME: Readonly<Record<string, string>> =
  Object.fromEntries(VALENTINE_POSES.map((p) => [p.filename, p.url]));
`;

await fs.mkdir(path.dirname(MANIFEST_OUT), { recursive: true });
await fs.writeFile(MANIFEST_OUT, header);

console.log(`Wrote ${MANIFEST_OUT}`);
const totalBytes = manifest.entries.reduce((a, b) => a + b.newBytes, 0);
console.log(`Uploaded ${entries.length} blobs (${(totalBytes / 1024 / 1024).toFixed(2)} MB total)`);
